def imageopen():
  root = tk.Tk()
  canvas = tk.Canvas(root)
  canvas.pack(expand = True, fill = tk.BOTH)
  image = ImageTk.PhotoImage(file="image/aarr.png")
  canvas_width = canvas.winfo_width()
  canvas_height = canvas.winfo_height()
  canvas.create_image(
                canvas_width / 2,       # 画像表示位置(Canvasの中心)
                canvas_height / 2,                   
                image=image  # 表示画像データ
                )
  root.mainloop()







